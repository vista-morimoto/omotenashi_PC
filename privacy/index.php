<dl id="personal">
<dt>１.個人情報の収集、利用及び提供</dt>
<dd>個人情報を収集する場合は、その収集目的を明確にした上で、必要な範囲の個人情報を収集させていただき、収集目的の範囲内で利用し、適切に管理いたします。<br />
また本人の同意を得ることなく個人情報を第三者に提供、開示等は一切いたしません。</dd>

