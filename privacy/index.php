<dd>個人情報保護を正確かつ安全に取り扱うために、個人情報保護に関する法令及びその他の規範を遵守し、適正な管理を行います。</dd>
 
<dt>４. 個人情報保護の実践と継続的改善 </dt>
<dd>個人情報保護実践のために、個人情報保護管理者を選任し具体的行動規範、ルールを定めるとともに定期的あるいは必要に応じ見直し、継続的改善を行います。<br />
また全ての教職員への個人情報保護教育・研修により個人情報保護方針の浸透を図ります。</dd>
</dl> 

