            <div class="Section clearfix">
                <h3 class="laceTit"><span>今、一緒に勉強している仲間が、大きな人脈になるんですよ</span></h3>
                <img src="../images/school/special/img_03.jpg" alt="在校生" class="Fright ml15 mb15" />
                <p><span>桂</span>　ぜひ、たくさんのことを学び取ってくださいね。私は学生時代に、校長先生から「“恕”という言葉を、覚えておきなさい」と教えられました。これは孔子の弟子が、「先生にいろんな教えを受けたけれど、自分たちは凡人だから、全てをまっとうすることができません。<br>今までの教義の中で、最も重要なことをひとつ挙げるとしたら、何でしょうか」と聞いたところ、孔子が「それは恕である」と答えたのです。恕とは、相手の立場に立ってものごとを考えること。<br>だから私はスタッフに、「いきなり“ノー”と言ってはいけません」と伝えています。まずは、顧客の皆様のご要望をきちんと聞く。最終的に不可能なことは、当然あります。だけど、一緒に「どうしたら実現できるだろう」と考えてあげることが大切なんです。</p>
                <p><span>吉田</span>　私たちの選んだ道は、みんな広い意味で接客業ですよね。先生は、どんなところで接客業の醍醐味を感じていますか？</p>
                <p><span>桂</span>　毎日、感じていますよ。ピエール・バルマンというフランスの著名なデザイナーが存命だった頃に、京都のウエディングドレスメーカーが、私と彼のドレスを扱っていたことがあるの。私はこの仕事を始めてまだ５年目くらいだったかしら。その会社が、料亭で私とバルマンを接待してくれたことがあるんです。<br>その時にバルマンが、「この世で一番美しいものは、花嫁姿だと思っている。貴女は、毎日ウエディングドレスの世界にいることができて、うらやましい」と言ったんですよ。彼の日常は他のお洋服のデザインで、オートクチュールでウエディングドレスをデザインするのは、それこそ王室や有名な芸能人くらいだから、そんなに機会が多くなかったみたい。フランスの大デザイナーが「うらやましい」と言ってくれる人生って、素晴らしいでしょ。<br>私はその時、自分の人生を見直しました。そして、「もう迷わない」と決めたんですよ。たくさんの人を幸せにしたり、気持ちよく過ごせる環境を作る仕事には、そういう喜びがあると思います。</p>
                <p><span>大塚</span>　学生時代に、自ら勉強したり、経験しておいたほうがいいことはありますか？</p>
                <p><span>桂</span>　私たちの世代が、一番できなかった事が語学なんですよ。これから先、英語が必要だということは皆さんもわかっていると思います。特にこの先は、絶対に必要ですね。<br>そして、情報はできるだけ集めておいたほうがいいと思います。世の中の流れがどう変わっていくかは、見極めておかないとね。このお仕事は、もちろんずっと存在するものではありますが、その中でも流行があるでしょうから。皆さんは、プロになったらどんなことをやってみたいのかしら。どんなプロになりたいと思っていますか？</p>
                <p><span>大塚</span>　僕は、みんなから憧れられるブライダルプランナーになりたいです。</p>
                <p><span>桂</span>　誰にプロデュースしてもらうか、そしてどんなプランを立ててくれるのかというのは、顧客の皆さんにとってはとても重要なことですからね。一億円の大金をかけても、「もっといい式ができたのに」と思うことだって、あるんです。そしてほとんどの人は、予算が限られています。<br>そういう意味で、これはブライダルだけじゃなく皆さんに共通して言えますが、人脈はとっても大事。例えば、添乗員とホテルが一緒になって、イベントやプランを立てることもできるじゃないですか。さらに言えば、限られた予算の中で、「こんなことをやりたい」というお願いを、知り合いが叶えてくれるかもしれません。今、一緒に勉強している仲間を、大切にしてくださいね。</p>
                <p><span>倉増</span>　私は、笑顔に触れることがとても好きなんです。目指しているコスチュームアドバイザーは、一番新婦さんに近づけますし、笑顔をサポートできると思うので、最高の笑顔をいただけるアドバイザーになりたいですね。</p>
                <p><span>桂</span>　日本では、ウエディングドレスを借りる人と買う人の比率は、だいたい３：１なんです。そして日本人の気質でいいところは、上を見るところ。予算が20万円しかないのに、「300万円のドレスを着たい」なんて、欧米の人は思わないの。<br>でも日本だと、レンタルでそれができる。さらに日本の家は狭いから、ドレスを置く場所もないので、レンタルが主流なんです。これは、日本人の知恵ですよね。ぜひ、花嫁さんが着たがっているドレスを、いい形でアドバイスしてあげてください。例えば最近のドレスの中には、結婚式ではロングだけど、途中から切り離してショートスタイルにもなり、そのまま二次会へ行けるものもあるんです。普通の人たちはそんなドレスがあることを知らないので、こういうことを教えてあげると、選択肢が広がると思いますよ。</p>
