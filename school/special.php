                <p><span>吉田</span>　ご葬儀もまた、結婚式と同じで一生に一度しかないものです。だからこそ、お客様の希望に臨機応変に合わせられる葬祭ディレクターになりたいと思います。</p>
                <p><span>桂</span>　ご葬儀も、多様化していますものね。例えばお葬式じゃなく、「偲ぶ会」としてみたり。これも、とてもいいことだと思います。私もお葬式じゃなく、ファッションショーをやってもらうことにしているの。あとは秋川雅史さんに、『千の風になって』を歌ってもらうのよ。その二つは、もう遺言に書いてあるの。「先生、まだ早いですよ」と言われましたけどね(笑)。</p>
                <p><span>古澤</span>　私は、先ほど先生がおっしゃられたように、「ノー」と言わず全てに対応できるホテルスタッフになりたいです。</p>
                <p><span>桂</span>　とっても、大切なことよ。特にホテルは、一切クレームが出ないことを重視しているから。一方で、ずっと保守的でいても小さな範囲で収まってしまう。問題が起きないように、人の２倍も３倍も働かなくちゃいけないかもしれない。でも、自分の考えを通すためには、そういう努力が必要なんですよ。そのためにも、健康でいることを心がけてください。</p>
